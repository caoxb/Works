package tarena.day02;

public class Test8 {
	public static void main(String[] args) {
		/*
		 * 00000000000000001
		 * 00000000000000010
		 * 00000000000001000 |
		 * ------------------------------
		 * 00000000000001011
		 */
		System.out.println(1 | 2 | 8);
		
		/*
		 * 1011			1011			1011
		 * 0010			1000			0100		&
		 * -------------------------------------
		 * 0010			1000			0000
		 */
		System.out.println(11 &  2);
		System.out.println(11 &  8);
		System.out.println(11 &  4);
		
		//////////////////////////////////////

		// 异或 - 相等得0，不等得1
		/*
		 * 0001011 - 11
		 * 0000110 - 6		^
		 * -------------------------
		 * 0001101 - 13
		 */
		System.out.println(11 ^ 6);
		
		//////////////////////////////////////
		/* 求反，0变1,1变0
		 * 0000000000000000000001
		 * 1111111111111111111110
		 */
		System.out.println(~1);
		
		//////////////////////////////////////
		// 000000000001 << 3
		// 000000001000 
		System.out.println(1 << 3);
		// 000100000000 >> 5
		// 000000001000
		System.out.println(256 >> 5);
		// 100000...000000 >> 28
		// 111111...111000
		System.out.println(0x80000000 >> 28);
		// 100000...000000 >>> 28
		// 000000...001000
		System.out.println(0x80000000 >>> 28);
		
		
		
		
	}
}
